function pipenvStart() {
    pipenv run jupyter notebook --port 3000;
}

function pipenvInstall() {
  groupLog "pipenvInstall";

  if [[ $(isTodayFolder '.venv') == 1 ]]
  then
    pipenv --rm;
    export PIPENV_VENV_IN_PROJECT=1;
    pipenv install;
  fi
}


function pyStart() {
    groupLog "pyStart";
    pipenvInstall || exit $?;
    pipenvStart || exit $?;
    exit 0;
}
