function isPipenvInstalled() {
    if [[ ! $(which pipenv;) ]]
    then
        echo "false";
    else
        echo "true";
    fi
}

function pyInstall() {
    groupLog "pyInstall";
    installed=$(isPipenvInstalled;);
    if [[ "${installed}" == "false" ]]
    then
      os=$(getOS;);
      ./fiddle.sh "setup" "${os}" "pipenv";
    fi
}
