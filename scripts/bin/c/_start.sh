function gccBuild() {
    if [[ -e ${__COMPILED_FILE__} ]]
    then
        rm -rf ${__COMPILED_FILE__};
    fi
    ${__C_COMPILER__} ${__SOURCE_FILE__} -o ${__COMPILED_FILE__};

}

function gccStart() {
  groupLog "gccStart";
  if [[ ! -e ".fiddlerc" ]]; then exit -1; fi
  source ".fiddlerc";
  if [[ -d "${__PROJECT_DIR__}" ]]
  then
    cd ${__PROJECT_DIR__};
    gccBuild || exit $?;
    if [[ ! -e ${__COMPILED_FILE__} ]]
    then
        exit -1;
    fi
    groupLog "Invoking executable";
    echo -e "$(./${__COMPILED_FILE__}) \n";
  fi
}
