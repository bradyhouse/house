function nvmInstall() {
  groupLog "nvmInstall";

  if [[ -d ${NVM_DIR} ]]
  then
    source ${NVM_DIR}/nvm.sh;
    nvm install ${NVM_VERSION};
  else
    exit 3;
  fi
}

function npmInstall() {
  groupLog "npmInstall";
  if [[ ! -d node_modules ]]
  then
    npm install;
  fi
}

function vueTest() {
    groupLog "vueTest";
    nvmInstall || exit $?;
    npmInstall || exit $?;
    npm test || exit $?;
    exit 0;
}
