function isJavacInstalled() {
  installed=$(if [[ ! -e "${__JAVA_COMPILER__}" ]]; then echo "false"; fi);
  if [[ "${installed}" == "false" ]]
  then
      exit -1;
  fi
}

function isJavaInstalled() {
  installed=$(if [[ ! -e "${__JAVA_RUNTIME__}" ]]; then echo "false"; fi);
  if [[ "${installed}" == "false" ]]
  then
      exit -1;
  fi
}