function kitchenConverge() {
  groupLog "kitchenConverge";
  kitchen converge;
}

function kitchenLogin() {
  groupLog "kitchenLogin";
  kitchen login;
}

function chefStart() {
    groupLog "chefStart";
    kitchenConverge || exit $?;
    kitchenLogin || exit $?;
    exit 0;
}
