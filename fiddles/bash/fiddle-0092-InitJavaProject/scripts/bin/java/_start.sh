function gradlewRun() {
  groupLog "gradlewRun";
  cd $1;
  #try
  (
    ./gradlew run;
  )
  rc=$?; case ${rc} in
    0)  groupLog "gradlew run - successful."
        ;;
    *)  exit -1;
        ;;
  esac

}

function javaStart() {
  groupLog "javaStart";
  if [[ ! -e ".fiddlerc" ]]; then exit -1; fi
  source ".fiddlerc";
  gradlewRun ${__PROJECT_DIR__} || exit $?;
}
